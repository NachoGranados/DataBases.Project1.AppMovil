package com.example.cinetec.models;

public class Cinema {

    private int number;
    private int rows;
    private int columns;
    private int capacity;
    private String nameMovieTheater;

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public int getRows() {
        return rows;
    }

    public void setRows(int rows) {
        this.rows = rows;
    }

    public int getColumns() {
        return columns;
    }

    public void setColumns(int columns) {
        this.columns = columns;
    }

    public int getCapacity() {
        return capacity;
    }

    public void setCapacity(int capacity) {
        this.capacity = capacity;
    }

    public String getNameMovieTheater() {
        return nameMovieTheater;
    }

    public void setNameMovieTheater(String nameMovieTheater) {
        this.nameMovieTheater = nameMovieTheater;
    }
}
